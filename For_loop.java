package javaprograms;
// Print 1 -100
public class For_loop {
	public static void main(String[] args) {
		for(int i = 1; i<=100;i++) {
			System.out.println(i);
			
		}
		//Print -10 to 10
		for(int i = -10; i<= 10 ;i++) {
			System.out.println(i);
		}
		// print 10 to -10
		for(int i=10; i<= -10 ;i--) {
			System.out.println(i);
		}
		//     Print 51 to positive infinite loop using for loop
		for(int i=51; i<= -51 ;i++) {
			
			System.out.println(i);
		}
		
	}
	

}
