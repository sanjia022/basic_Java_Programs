package basicjavaprogram;

public class ForLoopAssignment1 {
	
	public static void main(String[] args) {
		
		//   Print 1 to 100 using for loop
		
		for (int i= 1; i<= 100; i++) {
		System.out.println(i);
		
	}
		//Print -10 t o 10 using for loop
	for (int i= -10; i<= 10; i++) {
		System.out.println(i);
		
	}
		
		//Print 10 to -10 using for loop
		for (int i=10; i>= -10; i--) {
			System.out.println(i);
			
		}
		//
	for (int i=51; i>= -51; i++) {
		System.out.println(i );
		
	}
		
	}

}
